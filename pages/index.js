//server side rendering for this page alone:
// this function will get executed befor the upper part of this
// page will be rended to the screen
// the server render happens first, and than the information is
//send to the client 

export async function getServerSideProps(context) {
  //we pull the genre from the url
  const genre = context.query.genre;
  const reqest = await fetch(
    `https://api.themoviedb.org/3${
      requests[genre]?.url || requests.fetchTrending.url
    }`
    ).then((res) => res.json());

    return {
      props: {
        results: reqest.results,
      },
    };
}

